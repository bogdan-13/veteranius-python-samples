# Програма виводить всі числа від 10 до 100 кратні 5
for count in range(10, 100, 5):  # перший параметр - початок (10), другий кінець(100), третій - крок зміни(5) count
    print(count)  # Тіло циклу

print('The End')  # Частина програми, яка виконується після завершення циклу

# тут для перебору циклу значення змінній count надаємо в функції range:
# range([початок діапазону включно], [кінець діапазону не включно], [крок(на скільки наступне більше від попереднього)])
