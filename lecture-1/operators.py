number_a = 7  # Присвоюємо в змінну number_a число 7
number_b = 2  # Присвоюємо в змінну number_b число 2

text_a = 'Слава Україні!'  # Присвоюємо в змінну text_a текст 'Слава Україні!'
text_b = ' Героям слава!'  # Присвоюємо в змінну text_b текст ' Героям слава!'

result = number_a + number_b  # Додавання чисел
print('number_a + number_b = ', result)  # Виводимо результат на екран

result = number_a - number_b  # Віднімання чисел
print('number_a - number_b = ', result)  # Виводимо результат на екран

result = number_a * number_b  # Множення чисел
print('number_a * number_b = ', result)  # Виводимо результат на екран

result = number_a / number_b  # Ділення чисел
print('number_a / number_b  = ', result)  # Виводимо результат на екран

result = number_a ** number_b  # Піднесення до степеня (number_a в степені number_b)
print('number_a ** number_b = ', result)  # Виводимо результат на екран

result = number_a ** 0.5  # Корінь квадратний з числа number_a
print('number_a ** 0.5 = ', result)  # Виводимо результат на екран

result = number_a // number_b  # Цілочисельне ділення (ділення націло без остачі, остача відкидається)
print('number_a // number_b = ', result)  # Виводимо результат на екран

result = number_a % number_b  # Обчислення остачі від ділення number_a на number_b
print('number_a % number_b = ', result)  # Виводимо результат на екран

result = text_a + text_b  # З`єднання двох рядків (тексту)
print('text_a + text_b = ', result)  # Виводимо результат на екран

result = text_a * 10  # Повторення рядка (тексту) 10 разів.
print('text_a * 10 = ', result)  # Виводимо результат на екран
